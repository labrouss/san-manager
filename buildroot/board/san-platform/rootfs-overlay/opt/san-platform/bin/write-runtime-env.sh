#!/bin/sh
# =============================================================================
# /opt/san-platform/bin/write-runtime-env.sh
#
# Generates /opt/san-platform/.env before docker compose up so that:
#   • CORS_ORIGIN reflects the VM's actual IP address
#   • Secrets are read from /opt/san-platform/secrets/ (plain-text files
#     are acceptable here because the VM image is deployed as an appliance
#     and the secrets/ directory is chmod 700 root:root)
#
# Values marked CHANGE_ME must be updated by the operator after first boot:
#   /opt/san-platform/bin/san-platform-configure
# =============================================================================
set -e

ENV_FILE="/opt/san-platform/.env"
SECRETS_DIR="/opt/san-platform/secrets"
TEMPLATE="/opt/san-platform/.env.template"

log() { echo "[san-platform] $*"; }

# ── Detect primary IP ────────────────────────────────────────────────────────
PRIMARY_IP=$(ip route get 1.1.1.1 2>/dev/null \
    | awk '/src/ { for(i=1;i<=NF;i++) if($i=="src") print $(i+1) }' \
    | head -1)
PRIMARY_IP="${PRIMARY_IP:-127.0.0.1}"
log "Detected primary IP: ${PRIMARY_IP}"

# ── Read secrets ──────────────────────────────────────────────────────────────
DB_PASSWORD="san_secret"
MDS_PASSWORD="CHANGE_ME"

if [ -f "${SECRETS_DIR}/db_password.txt" ]; then
    DB_PASSWORD=$(cat "${SECRETS_DIR}/db_password.txt" | tr -d '[:space:]')
fi

if [ -f "${SECRETS_DIR}/mds_password.txt" ]; then
    MDS_PASSWORD=$(cat "${SECRETS_DIR}/mds_password.txt" | tr -d '[:space:]')
fi

# ── Read JWT secret (generate once if absent) ─────────────────────────────────
JWT_SECRET_FILE="${SECRETS_DIR}/jwt_secret.txt"
if [ ! -f "${JWT_SECRET_FILE}" ]; then
    log "Generating JWT secret…"
    # Use /dev/urandom — openssl may not be in PATH at this stage
    tr -dc 'A-Za-z0-9!@#$%^&*' < /dev/urandom | head -c 48 > "${JWT_SECRET_FILE}"
    chmod 600 "${JWT_SECRET_FILE}"
fi
JWT_SECRET=$(cat "${JWT_SECRET_FILE}")

# ── Write .env ────────────────────────────────────────────────────────────────
cat > "${ENV_FILE}" << EOF
# Auto-generated by write-runtime-env.sh on $(date -u)
# Edit /opt/san-platform/secrets/*.txt and reboot to change credentials.

# ── Database ──────────────────────────────────────────────────────────────────
DATABASE_URL=postgresql://san_admin:${DB_PASSWORD}@db:5432/san_zoning

# ── Backend ───────────────────────────────────────────────────────────────────
NODE_ENV=production
PORT=3001
LOG_LEVEL=info
CORS_ORIGIN=http://${PRIMARY_IP}:8080

# ── MDS Switches ──────────────────────────────────────────────────────────────
MDS_USERNAME=admin
MDS_PASSWORD=${MDS_PASSWORD}
MDS_PORT=443
MDS_SIMULATE=false

# ── Frontend port (host-side) ─────────────────────────────────────────────────
FRONTEND_PORT=8080

# ── Auth ──────────────────────────────────────────────────────────────────────
JWT_SECRET=${JWT_SECRET}
JWT_EXPIRES=8h
ADMIN_PASSWORD=Admin1234!

# ── Seeding ───────────────────────────────────────────────────────────────────
SAN_SEED=false
EOF

chmod 640 "${ENV_FILE}"
log ".env written to ${ENV_FILE}"
